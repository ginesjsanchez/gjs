unsigned int HashByte ( byte byVal ) 
{
    return ( (unsigned int) _hash_table [ byVal ] );
}

unsigned int HashCaracter ( char cVal )
{
    return ( HashByte ( (byte) cVal ) );
}

unsigned int HashBinario ( byte * p_byVal, int iTam )
{
	unsigned int uiRes = 0;
	
	if ( ES_VALIDO ( p_byVal ) && ( iTam > 0 ) )	
	{
		int iTamHash = sizeof( unsigned int );
		byte h[ iTamHash ];
		memset( h, 0, iTamHash );
		h[ 0 ] = _hash_table[ p_byVal[ 0 ] ];
		for ( int i = 1; i < iTam; i = i + 1 ) 
		{
			int slot = i % iTamHash;
			byte acc = p_byVal[ i ] ^ h[ slot ];
			/* mezcla con el slot anterior para difusi¾n entre carriles */
			if ( slot > 0 )
			{
				acc ^= h[ slot - 1 ];
			}
			h[ slot ] = _hash_table[ acc ];
		}
		for ( int i = 0; i < iTamHash; i = i + 1 )
		{
			uiRes |= (unsigned int) h[ i ] << ( i * 8 );
		}
	}
    return ( uiRes );
}

unsigned int HashEntero ( int iVal )
{
    return ( HashBinario ( (byte *) &iVal, sizeof ( int ) ) );
}

